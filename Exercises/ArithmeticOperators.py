numberOne = int(input('Type a number: '))
numberTwo = int(input('Type another number: '))

summ = numberOne + numberTwo
sub = numberOne - numberTwo
mult = numberOne * numberTwo
div = numberOne / numberTwo
pow = numberOne ** numberTwo
wholeDiv = numberTwo // numberTwo
restDiv = numberOne % numberTwo

print('Summ: {}'.format(summ))
print('Subtraction: {}'.format(sub))
print('Multiplication: {}'.format(mult))
print('Division: {}'.format(div))
print('Power: {}'.format(pow))
print('Whole division: {}'.format(wholeDiv))
print('Rest of division: {}'.format(restDiv))
